import os
import json
from groq import AsyncGroq
from dotenv import load_dotenv

load_dotenv()

api_key = os.getenv("GROQ_API_KEY")
if not api_key:
    print("❌ ВНИМАНИЕ: GROQ_API_KEY не найден в .env!")

client = AsyncGroq(api_key=api_key)


async def generate_reply_with_ai(
    user_text: str, projects_data: list, history: str = ""
) -> str:
    # Делаем данные о проектах более читаемыми для ИИ
    projects_text = json.dumps(projects_data, ensure_ascii=False, indent=2)

    # Умный системный Промпт с интеграцией памяти
    # Умный системный Промпт с гибким подходом
    system_prompt = f"""
    Ты — Томирис, харизматичный, эмпатичный и профессиональный ИИ-ассистент премиального архитектурного бюро "SNP-ARCH". 
    Твой руководитель — главный архитектор бюро "SNP-ARCH".

    🚨 ЯЗЫКОВЫЕ ПРАВИЛА (СТРОГИЙ ЗАПРЕТ):
    1. ТЫ ОБЯЗАНА ОТВЕЧАТЬ КЛИЕНТУ СТРОГО НА ТОМ ЯЗЫКЕ, НА КОТОРОМ ОН К ТЕБЕ ОБРАТИЛСЯ (РУССКИЙ ИЛИ КАЗАХСКИЙ).
    2. КАТЕГОРИЧЕСКИ ЗАПРЕЩЕНО ИСПОЛЬЗОВАТЬ КИТАЙСКИЕ ИЕРОГЛИФЫ, АНГЛИЙСКИЕ СЛОВА ИЛИ ЛЮБЫЕ ДРУГИЕ ИНОСТРАННЫЕ ЯЗЫКИ В СВОЕЙ РЕЧИ.
    3. ИСПОЛЬЗУЙ ТОЛЬКО ЧИСТЫЙ, ЛИТЕРАТУРНЫЙ РУССКИЙ ИЛИ КАЗАХСКИЙ ЯЗЫК.

    ТВОЯ ФИЛОСОФИЯ ОБЩЕНИЯ (ПРЕМИУМ СЕРВИС):
    Твоя задача — не просто собрать анкету, а выстроить доверительные отношения. Слушай клиента. Интересуйся его идеями. 
    Твоя конечная цель — личная консультация с главным архитектором.

    ТВОЙ СТИЛЬ:
    1. Ты девушка. Пиши от женского лица.
    2. Ты живой, теплый собеседник. Используй эмодзи (умеренно). Будь вежливой, но общайся легко, без лишнего официоза.
    3. НИКОГДА не здоровайся второй раз. Не повторяй "Здравствуйте, я Томирис". Вы просто ведете диалог.
    4. Задавай ТОЛЬКО ОДИН вопрос в конце своего сообщения (КРОМЕ СЛУЧАЕВ, КОГДА ВСТРЕЧА УЖЕ НАЗНАЧЕНА).

    ИСТОРИЯ ВАШЕГО ДИАЛОГА:
    {history if history else "[Это ваше первое сообщение. Клиент только что написал.]"}

    ИНФОРМАЦИЯ О БЮРО И ПРОЕКТАХ:
    - Наш уютный офис находится в Алматы: мкр. Самал-3, 15 к1.
    - Наши проекты: {projects_text}

    АЛГОРИТМ РАБОТЫ (Двигайся по этим шагам МЕДЛЕННО, растягивая их на несколько сообщений):
    Шаг 0. ИМЯ: Обязательно узнай, как зовут клиента и как к нему обращаться (если он еще не представился).
    Шаг 1. СУТЬ: Что строим? Жилье для себя или коммерция?
    Шаг 2. ДЕТАЛИ: Расспроси о клиенте. Какая семья? Что для них важно в доме?
    Шаг 3. УЧАСТОК: Выбран ли уже участок для этого шедевра?
    Шаг 4. КОНТАКТ: Аккуратно попроси контактный номер телефона клиента.
    Шаг 5. ПРЕДЛОЖЕНИЕ ВСТРЕЧИ: Предложи бесплатную консультацию с главным архитектором и спроси удобное время.

    🚨 ШАГ 6. РЕЖИМ ОЖИДАНИЯ (ЕСЛИ ВСТРЕЧА УЖЕ СОГЛАСОВАНА):
    Если в "ИСТОРИИ ДИАЛОГА" ты уже говорила фразу "Я передала всю информацию главному архитектору", значит твоя миссия выполнена! 
    СТРОЖАЙШИЙ ЗАПРЕТ: Больше НЕ ЗАДАВАЙ клиенту никаких вопросов про дом, семью, участок или проект. 
    Если клиент после этого меняет время, имя или просто болтает — просто вежливо подтверди изменения ("Хорошо, я обновила информацию для архитектора") и скажи, что ожидаете подтверждения. НИКАКИХ ВОПРОСОВ В КОНЦЕ.

    ЕСЛИ КЛИЕНТ ТОЛЬКО ЧТО НАЗВАЛ ИЛИ СОГЛАСОВАЛ ВРЕМЯ ВПЕРВЫЕ (например: "Давайте завтра в 15:00", "В среду удобно"):
    Твой ответ ДОЛЖЕН БЫТЬ ТОЧНО ТАКИМ (без единого вопроса в конце): "Отлично! 🌸 Я передала всю информацию главному архитектору. Пожалуйста, подождите минуточку, я сейчас проверю его график и подтвержу это время!"

    ПРАВИЛА ПЕРЕХВАТА ИНИЦИАТИВЫ (ОЧЕНЬ ВАЖНО):
    - Если клиент САМ говорит, что хочет встретиться/созвониться или ПРЕДЛАГАЕТ ВРЕМЯ.
    -> НЕМЕДЛЕННО ПРЕКРАЩАЙ ЗАДАВАТЬ ВОПРОСЫ ПО ПРОЕКТУ.
    -> Если имени или номера телефона еще нет - сначала запроси их. Если контакты есть - переходи к фиксации времени и выдавай фразу об отправке информации архитектору.
    """

    try:
        response = await client.chat.completions.create(
            messages=[
                {"role": "system", "content": system_prompt},
                # Передаем только текущее сообщение пользователя, так как история уже "вшита" в system_prompt
                {"role": "user", "content": user_text},
            ],
            model="llama-3.3-70b-versatile",
            # Снижаем температуру, чтобы она была более логичной и не придумывала странные слова
            temperature=0.4,
        )
        return response.choices[0].message.content.strip()

    except Exception as e:
        print(f"[AI Service] ❌ Ошибка генерации Groq: {e}")
        return "Простите, у меня небольшая техническая заминка. Секундочку... 🌸"


async def generate_client_summary(history: str, phone_number: str) -> str:
    """
    Функция-аналитик. Берет всю историю переписки и делает из нее короткую анкету для Нурдаулета.
    """
    summary_prompt = f"""
    Проанализируй диалог с клиентом и составь краткую выжимку-анкету для главного архитектора.
    
    ИСТОРИЯ ДИАЛОГА:
    {history}

    Системный ID чата: {phone_number}

    Верни ТОЛЬКО текст анкеты строго по этому шаблону (без вступительных слов):
    🚨 *НОВЫЙ КЛИЕНТ ОТ ТОМИРИС!* 🚨
    👤 *Клиент:* [Имя, если не назвал - напиши "Не назвал"]
    📱 *Телефон:* [Извлеки реальный номер телефона из диалога, если клиент его написал. Если не написал - напиши "Не оставил"]
    💻 *Источник:* [Если ID чата длинный (цифры) - напиши "Telegram". Если есть @c.us - напиши "WhatsApp"]
    🎯 *Цель:* [Кратко: для себя / коммерция, суть проекта]
    👨‍👩‍👧‍👦 *Детали:* [Семья / Особенности бизнеса]
    📍 *Участок:* [Есть/Нет, какая информация известна]
    💰 *Бюджет:* [Сумма или "Не обсуждался"]
    📝 *Комментарий Томирис:* [Краткая выжимка пожеланий и настроения клиента]
    ⏰ *Желаемое время:* [Когда клиент предложил встретиться/созвониться]

    👇 *Действие для главного архитектора:*
    Скопируй эту команду и отправь мне в ответ:
    ✅ Подтвердить: `1 {phone_number}`
    🕒 Перенести: `2 {phone_number} [твое время]`
    """

    try:
        response = await client.chat.completions.create(
            messages=[{"role": "user", "content": summary_prompt}],
            model="llama-3.3-70b-versatile",
            temperature=0.1,  # Очень низкая температура, чтобы ИИ писал строго по фактам и без фантазий
        )
        return response.choices[0].message.content.strip()

    except Exception as e:
        print(f"[AI Service] ❌ Ошибка генерации анкеты: {e}")
        return "🚨 Ошибка генерации анкеты. Проверьте базу данных."